function out = run_pyfai_multigeometry_from_matlab(imgPaths, poniPaths, wavelength_m, cfg)
% Runs pyFAI MultiGeometry via external python process, returns result in MATLAB.

if nargin < 4 || isempty(cfg), cfg = struct(); end
if ~iscell(imgPaths), imgPaths = cellstr(imgPaths); end
if ~iscell(poniPaths), poniPaths = cellstr(poniPaths); end
if numel(imgPaths) ~= numel(poniPaths)
    error("imgPaths and poniPaths must have same length");
end

% defaults
cfg = setDefault(cfg,"pythonExe","python");
cfg = setDefault(cfg,"scriptPath", fullfile(tempdir,"pyfai_multigeom_run.py"));
cfg = setDefault(cfg,"outBase", fullfile(pwd,"pyfai_multigeom_out"));
cfg = setDefault(cfg,"mode","2d");                 % "2d" or "1d"
cfg = setDefault(cfg,"unit","2th_deg");            % "2th_deg" or "q_A^-1"
cfg = setDefault(cfg,"npt_rad", 2000);
cfg = setDefault(cfg,"npt_azim", 360);
cfg = setDefault(cfg,"method","csr");
cfg = setDefault(cfg,"correctSolidAngle", true);
cfg = setDefault(cfg,"polarization_factor", NaN);
cfg = setDefault(cfg,"dummy", NaN);
cfg = setDefault(cfg,"delta_dummy", NaN);
cfg = setDefault(cfg,"dark", "");
cfg = setDefault(cfg,"flat", "");
cfg = setDefault(cfg,"mask", "");
cfg = setDefault(cfg,"chi_discont_deg", 180);
cfg = setDefault(cfg,"error_model", "poisson");
cfg = setDefault(cfg,"verbose", true);

cfg.outBase  = char(string(cfg.outBase));
cfg.pythonExe = char(string(cfg.pythonExe));
cfg.scriptPath = char(string(cfg.scriptPath));

cfg.outBase = char(string(cfg.outBase));

ensure_py_script(cfg.scriptPath);

job = struct();
job.img_paths = imgPaths;
job.poni_paths = poniPaths;
job.wavelength_m = wavelength_m;
job.mode = cfg.mode;
job.unit = cfg.unit;
job.npt_rad = cfg.npt_rad;
job.npt_azim = cfg.npt_azim;
job.method = cfg.method;
job.correctSolidAngle = cfg.correctSolidAngle;
job.polarization_factor = cfg.polarization_factor;
job.dummy = cfg.dummy;
job.delta_dummy = cfg.delta_dummy;
job.dark = cfg.dark;
job.flat = cfg.flat;
job.mask = cfg.mask;
job.chi_discont_deg = cfg.chi_discont_deg;
job.error_model = cfg.error_model;

job.out_mat  = char(string(cfg.outBase) + ".mat");
job.out_npz  = char(string(cfg.outBase) + ".npz");
job.out_json = char(string(cfg.outBase) + ".json");

jobJsonPath = char(string(cfg.outBase) + "_job.json");
write_json(jobJsonPath, job);

cmd = sprintf('"%s" "%s" "%s"', cfg.pythonExe, cfg.scriptPath, jobJsonPath);
if cfg.verbose
    fprintf("Running: %s\n", cmd);
end
[status, msg] = system(cmd);
if status ~= 0
    error("pyFAI MultiGeometry failed.\nCommand:\n%s\n\nOutput:\n%s", cmd, msg);
end
if cfg.verbose
    fprintf("%s\n", msg);
end

outMatPath = char(string(job.out_mat));
if ~exist(outMatPath,"file")
    error("Python did not create MAT file: %s\nCheck Python console output above.", outMatPath);
end
% S = load(outMatPath);

S = load(char(string(job.out_mat)));
out = S;                       % enthält I, radial, (azimuthal)
out.meta = read_json(job.out_json);
% Am Ende von run_pyfai_multigeometry_from_matlab:
out.matPath      = job.out_mat;    % Pfad zur gecakten .mat
out.rawStackPath = strrep(job.out_mat, '.mat', '_raw_stack.mat');
out.ringPath     = strrep(job.out_mat, '.mat', '_ring.mat');

% Geometrie-Parameter aus erster PONI-Datei lesen
% (für Ring-Darstellung in MATLAB)
try
    poni1   = poniPaths{1};
    fid     = fopen(poni1, 'r');
    txt     = fread(fid, '*char')';
    fclose(fid);

    % Distance (m -> mm)
    tok = regexp(txt, 'Distance:\s*([\d.eE+-]+)', 'tokens');
    if ~isempty(tok), out.sdd_mm = str2double(tok{1}{1}) * 1000; end

    % Poni1, Poni2 (m -> mm) = Strahlmitte auf Detektor
    tok = regexp(txt, 'Poni1:\s*([\d.eE+-]+)', 'tokens');
    if ~isempty(tok), out.center_y_mm = str2double(tok{1}{1}) * 1000; end
    tok = regexp(txt, 'Poni2:\s*([\d.eE+-]+)', 'tokens');
    if ~isempty(tok), out.center_x_mm = str2double(tok{1}{1}) * 1000; end
catch
    out.sdd_mm      = [];
    out.center_x_mm = [];
    out.center_y_mm = [];
end

end

%% helpers
function cfg = setDefault(cfg, field, val)
if ~isfield(cfg,field) || isempty(cfg.(field))
    cfg.(field) = val;
end
end

function ensure_py_script(scriptPath)
if exist(scriptPath,"file"), return; end
fid = fopen(scriptPath,"w");
assert(fid>0, "Cannot write python script to %s", scriptPath);
fwrite(fid, py_script_contents());
fclose(fid);
end

