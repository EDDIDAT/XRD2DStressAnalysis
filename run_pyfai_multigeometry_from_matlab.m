function write_json(path, s)
    path = char(string(path));

    % Ordner anlegen falls nicht vorhanden
    folder = fileparts(path);
    if ~isempty(folder) && ~exist(folder, 'dir')
        [ok, msg] = mkdir(folder);
        if ~ok
            error('write_json: Ordner konnte nicht angelegt werden:\n%s\nFehler: %s', folder, msg);
        end
    end

    % JSON schreiben
    fid = fopen(path, 'w');
    if fid == -1
        error('write_json: Datei konnte nicht geöffnet werden:\n%s', path);
    end

    try
        txt = jsonencode(s);
        fwrite(fid, txt, 'char');
        fclose(fid);
    catch ME
        fclose(fid);
        rethrow(ME);
    end

    % Sofort prüfen ob Datei wirklich existiert
    if ~exist(path, 'file')
        error('write_json: Datei wurde nicht erstellt:\n%s', path);
    end
end

function s = read_json(path)
s = jsondecode(fileread(path));
end
